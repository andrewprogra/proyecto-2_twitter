public class usuarios {
    String nombre;
    String username;
    String password;
    String genero;
    int edad;

    public usuarios(String nombre, String username, String password, String genero, int edad) {
        this.nombre = nombre;
        this.username = username;
        this.password = password;
        this.genero = genero;
        this.edad = edad;
    }

    usuarios() {
       
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
}
